function sumOfBig(firstNum, secondNum) {
  return BigInt(firstNum) + BigInt(secondNum);
}

function multiplyOfBig(firstNum, secondNum) {
  return BigInt(firstNum) * BigInt(secondNum);
}

function subtractionOfBig(firstNum, secondNum) {
  return BigInt(firstNum) - BigInt(secondNum);
}

function divisionOfBig(firstNum, secondNum) {
  return BigInt(firstNum) / BigInt(secondNum);
}
